package package1;

public class Datatytpes {
    public static void main(String[] args) {
        //boolean example
        boolean IOT = true;
        System.out.println(IOT);

        //byte example
        byte range;
        range=127;
        System.out.println(range);

        //short example
        short range1; 
        range1 =-127;
        System.out.println(range1);

        //int example
        int range2 = 303030303;
        System.out.println(range2);

        //long example
        long range3 = 4567894445757l;
        System.out.println(range3);

        //double example
        double number=99.9;
        System.out.println(number);

        //float example
        float number1=99.9f;
        System.out.println(number1);

        //char example
        char letter='\u0041';
        System.out.println(letter);
    }
}
